-- Bond Settlement System - Oracle DDL

-- Settlement Instruction table (business entity — message-format agnostic)
CREATE TABLE SETTLEMENT_INSTRUCTION (
    ID                  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    TRADE_REF           VARCHAR2(50)  NOT NULL,
    ISIN                VARCHAR2(12)  NOT NULL,
    SETTLEMENT_DATE     DATE          NOT NULL,
    QUANTITY            NUMBER(18,2)  NOT NULL,
    COUNTERPARTY        VARCHAR2(100) NOT NULL,
    BIC_CODE            VARCHAR2(11)  NOT NULL,
    DIRECTION           VARCHAR2(10)  NOT NULL,
    STATUS              VARCHAR2(20)  DEFAULT 'PENDING' NOT NULL,
    ACCOUNT_ID          VARCHAR2(50)  NOT NULL,
    PREFERRED_STANDARD  VARCHAR2(5)   DEFAULT 'MT' NOT NULL,
    RETRY_COUNT         NUMBER(3)     DEFAULT 0 NOT NULL,
    FAILURE_REASON      VARCHAR2(1000),
    CREATED_AT          TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT          TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UK_TRADE_REF UNIQUE (TRADE_REF),
    CONSTRAINT CK_DIRECTION CHECK (DIRECTION IN ('BUY', 'SELL')),
    CONSTRAINT CK_STATUS CHECK (STATUS IN ('PENDING', 'SUBMITTING', 'SENT', 'MATCHED', 'FAILED', 'RETRYING', 'CANCELLED')),
    CONSTRAINT CK_PREFERRED_STD CHECK (PREFERRED_STANDARD IN ('MT', 'MX'))
);

CREATE INDEX IDX_INSTRUCTION_STATUS ON SETTLEMENT_INSTRUCTION (STATUS);
CREATE INDEX IDX_INSTRUCTION_ISIN ON SETTLEMENT_INSTRUCTION (ISIN);
CREATE INDEX IDX_INSTRUCTION_DATE ON SETTLEMENT_INSTRUCTION (SETTLEMENT_DATE);

-- SWIFT Message store (decoupled from business entity, supports MT and MX)
CREATE TABLE SWIFT_MESSAGE (
    ID                NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    INSTRUCTION_ID    NUMBER         NOT NULL,
    TRADE_REF         VARCHAR2(50)   NOT NULL,
    MESSAGE_STANDARD  VARCHAR2(5)    NOT NULL,
    MESSAGE_TYPE      VARCHAR2(50)   NOT NULL,
    DIRECTION         VARCHAR2(10)   NOT NULL,
    RAW_PAYLOAD       CLOB           NOT NULL,
    NAMESPACE         VARCHAR2(200),
    BUSINESS_AREA     VARCHAR2(10),
    PARSED_STATUS     VARCHAR2(30),
    PARSED_REASON     VARCHAR2(500),
    SEQUENCE_NO       NUMBER(5)      DEFAULT 1 NOT NULL,
    CREATED_AT        TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_MSG_INSTRUCTION FOREIGN KEY (INSTRUCTION_ID)
        REFERENCES SETTLEMENT_INSTRUCTION(ID),
    CONSTRAINT CK_MSG_STANDARD CHECK (MESSAGE_STANDARD IN ('MT', 'MX')),
    CONSTRAINT CK_MSG_DIRECTION CHECK (DIRECTION IN ('OUTBOUND', 'INBOUND'))
);

CREATE INDEX IDX_MSG_INSTRUCTION ON SWIFT_MESSAGE (INSTRUCTION_ID);
CREATE INDEX IDX_MSG_TRADE_REF ON SWIFT_MESSAGE (TRADE_REF);
CREATE INDEX IDX_MSG_TYPE ON SWIFT_MESSAGE (MESSAGE_TYPE, DIRECTION);

